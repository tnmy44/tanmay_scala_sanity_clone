object join_by_c_string_and_first_name {

  def apply(context: Context, in0: DataFrame, in1: DataFrame): DataFrame =
    in0
      .as("in0")
      .join(in1.as("in1"),
            col("in0.c_string") =!= col("in1.first_name"),
            "inner"
      )

}
